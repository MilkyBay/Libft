#include "libft.h"

void	*ft_memchr(const void *arr, int c, size_t len)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)arr;
	while (i++ < len)
		if (*(ptr++) == (unsigned char)c)
			return ((void *)--ptr);
	return (0);
}
